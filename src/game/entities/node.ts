/** 地图上的一个位置。33 个节点组成路线，玩家逐节点推进。 */
export interface Node {
    /** 本节点绑定的事件 ID 列表。
     *
     *  - undefined / [] : 无固定事件，从事件池随机抽 3 个，生成「选择事件」轮。
     *  - ['xxx']        : 固定 1 个事件，跳过选事件轮，直接开始该事件。
     *  - ['a','b','c']  : 固定 3 个事件。
     *
     *  骨架层 + 故事叠加层决定，叠加后锁定。 */
    eventIds?: string[]
}
