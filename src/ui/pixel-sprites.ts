/** 基础角色（白色主色 + index 6 为点缀色） */
export function makeCharacterSprite(accentColor: string): PixelSprite {
    const palette = { ...MONO_PALETTE, '6': accentColor }
    return {
        palette,
        frames: { idle: IDLE, attack: ATTACK, hit: HIT },
    }
}

